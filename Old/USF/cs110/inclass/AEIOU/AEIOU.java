package AEIOU;
import java.util.Scanner;
public class AEIOU {

	public static void main(String[] args) {
		
		Scanner s = new Scanner(System.in);
		int a = 0, e = 0, i = 0, o = 0, u = 0, con = 0;
		
		System.out.println("Enter a string");
		String input = s.nextLine();
		
		for (int x = 0; input.length() > x; x++) {
			
			if (input.charAt(x) == 'a') a++;
			else if (input.charAt(x) == 'e') e++;
			else if (input.charAt(x) == 'i') i++;
			else if (input.charAt(x) == 'o') o++;
			else if (input.charAt(x) == 'u') u++;
			else con++;
		}
		
		System.out.println("Number of A's = " + a);
		System.out.println("Number of E's = " + e);
		System.out.println("Number of I's = " + i);
		System.out.println("Number of O's = " + o);
		System.out.println("Number of U's = " + u);
		System.out.println("Number of of other characters = " + con);
		
	}

}
