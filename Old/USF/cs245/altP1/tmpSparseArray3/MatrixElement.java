
public class MatrixElement<E> implements MatrixElem {
	int row, col;
	E value;
	
	public MatrixElement(int row, int col, E value) {
		this.row = row;
		this.col = col;
		this.value = value;
	}
	
	@Override
	public int columnIndex() {
		return col;
	}

	@Override
	public int rowIndex() {
		return row;
	}

	@Override
	public E value() {
		return value;
	}

}
